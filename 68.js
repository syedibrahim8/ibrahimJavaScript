// JSON vs Object 

/*
FEATURE                                JSON                              Object
What it is           Text format for storing/transferring data         Structured storage system (usually in databases)
Usage                   For APIs, configs, and communication           For querying and persistent data storage
Structure                         key-value pairs                     Row & columns(SQL) or documents(NoSQL)
Language support        Universal(Javascript, Python, etc)            Depends on DB engine(SQL, MongoDB,etc)
Editable by humans?                     Yes                           Usually va queries or admin tables
Support functions?                       No                           Some times(e.g., SQL functions, triggers)   

*/

