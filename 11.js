// Assignmet Operator
/*
 | Operator | Example  |   Same as   |
 | -------- | -------  | ----------- |
 |    =     |  x = 5   |  x = 5      |
 |    +=    |  x += 5  |  x = x + 5  |
 |    -=    |  x -= 5  |  x = x - 5  |
 |    *=    |  x *= 5  |  x = x * 5  |
 |    /=    |  x /= 5  |  x = x / 5  |
 |    **=   |  x **= 5 |  x = x ** 5 |
*/

// Example 1
 let box = 5;
 console.log(box);
 console.log(box+=5);
 console.log(box-=2);
 console.log(box*=3);
 console.log(box/=2);
 console.log(box**=2);

// Example 2
 let bin = 5;
 console.log(bin);
 bin += 5;
 console.log(bin);
 bin -= 2;
 console.log(bin);
 bin *= 3;
 console.log(bin);
 bin /= 2;
 console.log(bin);
 bin **= 2;
 console.log(bin);



