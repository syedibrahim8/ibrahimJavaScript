// return keyword in functions

function sum(a, b){
    return a + b;
}
let total = sum(3, 6);
console.log(total*2);

//

function add(x, y){
    a = x + y;
    return a;
}
let final = add(4, 5);
console.log(final*3);

// Note:
// 1. use return to pass the value.
// 2. you cannot write anything after return.