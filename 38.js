// push ()
// an array method which adds element at last
let arr = [10,20,30,60];
arr.push(40);
console.log(arr);

// pop()
// an array method which deletes element at last
let arr1 = [10,20,30];
arr1.pop();
console.log(arr1);

// unshift()
// adds an element at beginning
let arr2 = [10,20,30];
arr2.unshift(5);
console.log(arr2);

// shift()
// deletes an element at beginning
let arr3 = [10,20,30];
arr3.shift();
console.log(arr3);